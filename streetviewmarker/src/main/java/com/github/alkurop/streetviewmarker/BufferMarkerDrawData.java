public class BufferMarkerDrawData {
    public double left;
    public double top;
    public double right;
    public double bottom;

    public BufferMarkerDrawData(double left, double top, double right, double bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
